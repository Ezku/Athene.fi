<div id="post-body-content">    
    
    
    <p>Custom Content Types is a plugin designed to allow WordPress administrators to create and utilize the power of custom post types within WordPress. Now with the release of WordPress 3.0 it is possible to create more than just posts and pages, developers can define their own content types with their own options and custom taxonomies. These powerful features allow WordPress to be more than just blogging software but a content rich, user-friendly CMS. Now you can define products for an ecommerce site or books for keeping inventory, the possibilities are endless.</p>
            
    <h3>Usage</h3>
    
    <h4>Creating Content Types</h4>
        
    <p>After activating the <b>Custom Content Types</b> plugin, simply click on the <b>Content Types</b> item in the admin menu to see a list of current custom content types and a form to create new ones.</p>
        
    <p>Upon creating new content types they will appear in the admin menu in their own panel where you can create and modify posts of that type.</p>
        
